package DTO;

/**
 *
 * @author darker
 */
public class PartesDto {
    
    private int  idparte;
    private String tipo;
    private String serial;
    private String nomparte;
    private int hojadevida;

    /**
     * @return the idparte
     */
    public int getIdparte() {
        return idparte;
    }

    /**
     * @param idparte the idparte to set
     */
    public void setIdparte(int idparte) {
        this.idparte = idparte;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the serial
     */
    public String getSerial() {
        return serial;
    }

    /**
     * @param serial the serial to set
     */
    public void setSerial(String serial) {
        this.serial = serial;
    }

    /**
     * @return the nomparte
     */
    public String getNomparte() {
        return nomparte;
    }

    /**
     * @param nomparte the nomparte to set
     */
    public void setNomparte(String nomparte) {
        this.nomparte = nomparte;
    }

    /**
     * @return the hojadevida
     */
    public int getHojadevida() {
        return hojadevida;
    }

    /**
     * @param hojadevida the hojadevida to set
     */
    public void setHojadevida(int hojadevida) {
        this.hojadevida = hojadevida;
    }
    
    
}
